const langOrder = ["en", "zh-CN", "zh-TW"];
const langLabels = { en: "English", "zh-CN": "简体中文", "zh-TW": "繁體中文" };

let currentLang = localStorage.getItem("financeTrackerLang") || "en";

const t = (key) => {
  return translations[currentLang][key] || translations["en"][key] || key;
};

const applyTranslations = () => {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(key);
  });

  document.documentElement.lang = currentLang;

  const langBtn = document.getElementById("langToggleBtn");
  if (langBtn) {
    const nextIndex = (langOrder.indexOf(currentLang) + 1) % langOrder.length;
    langBtn.textContent = langLabels[langOrder[nextIndex]];
  }
};

const toggleLanguage = () => {
  const nextIndex = (langOrder.indexOf(currentLang) + 1) % langOrder.length;
  currentLang = langOrder[nextIndex];
  localStorage.setItem("financeTrackerLang", currentLang);
  applyTranslations();
};

if (typeof module !== "undefined" && module.exports) {
  module.exports = { t, applyTranslations, toggleLanguage, translations, currentLang };
}
